
package com.calculadora.logicanegocio;

/**
 * Esta clase contiene las 4 operaciones fundamentales de 
 * la aritmetica
 * 
 * @author anthonyM
 * 
 * @version 1.0.20
 */
public class CalculadoraBasica {
    
    /**
     * Este metodo realiza la suma de dos numeros enteros
     * @param a valor a sumar
     * @param b valor a sumar
     * @return devuelve el resultado
     */
    
    public int sumar(int a, int b){
        return a + b;
    
    }
    
    
    /**
     * Este metodo realiza la suma de dos numeros double
     * @param a valor a sumar
     * @param b valor a sumar
     * @return devuelve el resultado
     */
    public double sumar(double a, double b){
        return a + b;
        
    }
    
    /**
     * Este metodo realiza la resta de dos numeros enteros
     * @param a valor a restar
     * @param b valor a restar
     * @return devuelve el resultado
     */
        
    public int restar(int a, int b){
        return a-b;
    
    }
    
    
    /**
     * Este metodo realiza la resta de dos numeros double
     * @param a valor a restar
     * @param b valor a restar
     * @return devuelve el resultado
     */
    public double restar(double a, double b){
        return a-b;
    
    }
     
    
    /**
     * Este metodo realiza la divisio de dos numeros enteros
     * @param a valor a dividir
     * @param b valor a dividir
     * @return devuelve el resultado
     */
    public int dividir(int a, int b){
        return a/b;
    
    }
       
    
    /**
     * Este metodo realiza la division de dos numeros double
     * @param a valor a dividir
     * @param b valor a dividir
     * @return devuelve el resultado
     */
    public double dividir(double a, double b){
        return a/b;
    
    }
    
    /**
     * Este metodo realiza la multiplicacion de dos numeros enteros
     * @param a valor a multiplicar
     * @param b valor a multiplicar
     * @return devuelve el resultado
     */
    
    public int multiplicar(int a, int b){
        return a*b;
    
    }
    
      /**
     * Este metodo realiza la multiplicacion de dos numeros double
     * @param a valor a multiplicar
     * @param b valor a multiplicar
     * @return devuelve el resultado
     */
        
    public double multiplicar(double a, double b){
        return a*b;
        
     }
    
    
    
}//
